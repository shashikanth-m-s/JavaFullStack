package com.lab;

public class PrimitiveDataTypes1 {

	public static void main(String[] args) {
		boolean X=true;
		System.out.println(" boolean is "+X);
		
		byte range;
		range=124;
		System.out.println("range of byte is"+range);
		
		short range1;
		range1=-127;
		System.out.println("range of short is"+range1);
		
		int range2=3000000;
		System.out.println("range of int is"+range2);
		
		long range3=300000000000000L;
		System.out.println("range of long is"+range3);
		
		double number=99.3;
		System.out.println("double is"+number);
		
		float x1 =34.9f;
		System.out.println(" float is "+x1);
		
		char letter='v';
		System.out.println(" char is "+letter);
		
		String name="shailu";
		System.out.println("name is"+name);
		
		char letter2 = 65;
		System.out.println(" character is "+letter2);
		
		char letter6 = '\u0041';
		System.out.println(" character is "+letter6);

	}

}
